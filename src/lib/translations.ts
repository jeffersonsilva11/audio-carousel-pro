export function t(
  section: TranslationKey,
  key: string,
  language: SupportedLanguage
): string {
  const sectionData = translations[section] as Record<string, Record<SupportedLanguage, string>>;
  if (!sectionData || !sectionData[key]) {
    console.warn(`Translation missing: ${section}.${key}`);
    return key;
  }
  return sectionData[key][language] || sectionData[key]["pt-BR"] || key;
}
