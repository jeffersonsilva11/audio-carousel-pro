const TrustedBy = () => {
  const { language } = useLanguage();
  const { getContent } = useLandingContent();
  const [companies, setCompanies] = useState<TrustedCompany[]>([]);
  const [loading, setLoading] = useState(true);

  // Get dynamic content from database
  const sectionTitle = getContent("trusted_by", "title", language) || (
    language === "pt-BR" ? "Tecnologia usada por" : language === "es" ? "Tecnología usada por" : "Technology used by"
  );
  const sectionSubtitle = getContent("trusted_by", "subtitle", language) || (
    language === "pt-BR" ? "Powered by tecnologias de IA líderes do mercado" : language === "es" ? "Powered by tecnologías de IA líderes del mercado" : "Powered by market-leading AI technologies"
  );

  useEffect(() => {
    fetchCompanies();
  }, []);

  const fetchCompanies = async () => {
    try {
      const { data, error } = await supabase
        .from("trusted_companies")
        .select("*")
        .eq("is_active", true)
        .order("display_order");

      if (error) throw error;
      setCompanies(data || []);
    } catch (error) {
      console.error("Error fetching trusted companies:", error);
    } finally {
      setLoading(false);
    }
  };

  // Use database data if available, otherwise use fallback
  const displayCompanies = companies.length > 0 
    ? companies 
    : fallbackCompanies.map((c, i) => ({ id: String(i), name: c.name, logo_svg: c.logo_svg, display_order: i }));

  if (loading) {
    return (
      <section className="py-16 md:py-24 bg-background border-t border-border/50">
        <div className="container mx-auto px-4">
          <div className="animate-pulse flex items-center justify-center gap-8 md:gap-12 lg:gap-16">
            {[...Array(6)].map((_, i) => (
              <div key={i} className="h-8 w-24 bg-muted rounded" />
            ))}
          </div>
        </div>
      </section>
    );
  }

  return (
    <section className="py-16 md:py-24 bg-background border-t border-border/50">
      <div className="container mx-auto px-4">
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          whileInView={{ opacity: 1, y: 0 }}
          transition={{ duration: 0.5 }}
          viewport={{ once: true }}
          className="text-center mb-12"
        >
          <span className="text-sm font-medium text-muted-foreground uppercase tracking-wider">
            {sectionTitle}
          </span>
        </motion.div>

        <motion.div
          initial={{ opacity: 0 }}
          whileInView={{ opacity: 1 }}
          transition={{ duration: 0.5, delay: 0.2 }}
          viewport={{ once: true }}
          className="flex flex-wrap items-center justify-center gap-8 md:gap-12 lg:gap-16"
        >
          {displayCompanies.map((company, index) => (
            <motion.div
              key={company.id}
              initial={{ opacity: 0, y: 10 }}
              whileInView={{ opacity: 1, y: 0 }}
              transition={{ duration: 0.3, delay: index * 0.05 }}
              viewport={{ once: true }}
              className="text-muted-foreground/50 hover:text-muted-foreground transition-colors duration-300"
              title={company.name}
              dangerouslySetInnerHTML={{ __html: sanitizeSvg(company.logo_svg) }}
            />
          ))}
        </motion.div>

        <motion.p
          initial={{ opacity: 0 }}
          whileInView={{ opacity: 1 }}
          transition={{ duration: 0.5, delay: 0.5 }}
          viewport={{ once: true }}
          className="text-center text-xs text-muted-foreground/50 mt-8"
        >
          * {sectionSubtitle}
        </motion.p>
      </div>
    </section>
  );
};

export default TrustedBy;
